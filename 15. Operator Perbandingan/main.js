// Operator perbandingan
// =   Deklarasi
// ==  Sama dengan
// === Sama dengan tipe data dan nilai
// !=  Tidak sama dengan
// !== Tidak sama dengan tipe data dan nilai

let angka = "10";
let hewan = "Ayam";

if (angka == 10) {
    console.log("Benar!")
} else {
    console.log("Salah!")
}

if (angka === 10) {
    console.log("benar!");
} else {
    console.log("Salah!")
}

if (hewan === "ayam") {
    console.log("Benar!")
} else {
    console.log("Salah!")
}

if (hewan !== "Ayam") {
    console.log("benar!");
} else {
    console.log("Salah!")
}
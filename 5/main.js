//  Semua operator
//  +  = Tambah
//  -  = Kurang
//  *  = Kali
//  /  = Bagi
//  ** = Pangkat
//  %  = Sisa Bagi

let tambah = 15 + 15;
let kurang = 20 - 15;
let kali = 15 * 15;
let bagi = 30 / 15;
let pangkat = 2 ** 4;
let sisaBagi = 30 % 2;

console.log(tambah);
console.log(kurang);
console.log(kali);
console.log(bagi);
console.log(pangkat);
console.log(sisaBagi);
// While
let no = 1;
while (no <= 10) {
  console.log(`No. ${no}`);
  no++;
};

// Do While
// Iterasi di lakukan setidaknya sekali saat program di jalankan, jika kondisi terpenuhi akan melanjutkan iterasi sebelumnya.
let iteration = 1
do {
  console.log("Sekarang Adalah Loop Ke-" + iteration)
  iteration++
} while (iteration <= 10)
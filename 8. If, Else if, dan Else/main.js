// If, else if, dan else

let nilai = 80;

if (nilai >= 80 && nilai <= 100) {
  console.log("Nilai Baik.");
} else if (nilai >= 60 && nilai <= 80) {
  console.log("Nilai Cukup Baik.");
} else if (nilai >= 0 && nilai <= 60) {
  console.log("Nilai Kurang Baik.");
} else {
  console.log("Nilai Tidak Valid.");
}
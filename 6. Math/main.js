// Math

let x = 893;
let y = 231;
let z = 383;

x = Math.round(x); // Dibulatkan ke yang terdekat.
x = Math.floor(x); // Dibulatkan ke bawah.
x = Math.ceil(x); // Dibulatkan ke atas.
x = Math.trunc(x); // Dibulatkan ke bawah.
x = Math.pow(x, y); // Nilai x pangkat y.
x = Math.sqrt(x); // Menghitung akar kuadrat.
x = Math.abs(x); // Membalikan positif atau negatif nilai.
x = Math.max(x, y, z); // Mencari nilai tertinggi.
x = Math.min(x, y, z); // Mencari nilai terendah.

x = (x + z) * y;

console.log(x);